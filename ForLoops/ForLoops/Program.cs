namespace ForLoops
{
    internal class Program
    {
        static void Main(string[] args)
        {
            /*
             * For Loops
             * 
             * 
             */


            for (int i = 0;i<=10;i++)
            {
                Console.WriteLine("The value {0}",i);
            }
            for (int i = 10; i >0; i--)
            {
                Console.WriteLine("The value {0}", i);
            }
        }
    }
}
